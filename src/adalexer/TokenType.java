package adalexer;

/**
 * Перечисление всех возможных типов лексем языка Ada.
 * Каждая константа представляет уникальный тип токена,
 * который может распознать лексический анализатор.
 *
 * Группы токенов:
 * - Ключевые слова (79 штук)
 * - Идентификаторы и литералы
 * - Мультисимвольные операторы (:=, =>, .., **, <>, >=, <=, /=, <<, >>)
 * - Односимвольные операторы и разделители
 * - Специальные токены (EOF - конец файла, ERROR - ошибка)
 */
public enum TokenType {

    // ==================== КЛЮЧЕВЫЕ СЛОВА (регистронезависимые) ====================
    // Полный список ключевых слов Ada согласно стандарту ISO/IEC 8652
    KW_ABORT,      // abort - аварийное завершение
    KW_ABS,        // abs - абсолютное значение
    KW_ABSTRACT,   // abstract - абстрактный тип/операция
    KW_ACCEPT,     // accept - принятие вызова задачи
    KW_ACCESS,     // access - тип доступа (указатель)
    KW_ALL,        // all - все компоненты
    KW_AND,        // and - логическое И
    KW_ARRAY,      // array - массив
    KW_AT,         // at - размещение в памяти
    KW_BEGIN,      // begin - начало блока
    KW_BODY,       // body - тело пакета/задачи
    KW_CASE,       // case - условный оператор выбора
    KW_CONSTANT,   // constant - константа
    KW_DECLARE,    // declare - секция объявлений
    KW_DELAY,      // delay - задержка выполнения
    KW_DELTA,      // delta - точность фиксированной точки
    KW_DIGITS,     // digits - точность числа с плавающей точкой
    KW_DO,         // do - начало цикла/приемного оператора
    KW_ELSE,       // else - иначе
    KW_ELSIF,      // elsif - иначе если
    KW_END,        // end - конец конструкции
    KW_ENTRY,      // entry - запись (точка входа) задачи
    KW_EXCEPTION,  // exception - исключение
    KW_EXIT,       // exit - выход из цикла
    KW_FOR,        // for - цикл с параметром
    KW_FUNCTION,   // function - функция
    KW_GENERIC,    // generic - обобщённый пакет/подпрограмма
    KW_GOTO,       // goto - безусловный переход
    KW_IF,         // if - условный оператор
    KW_IN,         // in - параметр "на вход"
    KW_INTERFACE,  // interface - интерфейс
    KW_IS,         // is - связка (is new, is record и др.)
    KW_LIMITED,    // limited - ограниченный тип
    KW_LOOP,       // loop - цикл
    KW_MOD,        // mod - модуль (остаток от деления)
    KW_NEW,        // new - создание объекта
    KW_NOT,        // not - логическое НЕ
    KW_NULL,       // null - нулевое значение
    KW_OF,         // of - часть агрегата
    KW_OR,         // or - логическое ИЛИ
    KW_OTHERS,     // others - остальные варианты
    KW_OUT,        // out - параметр "на выход"
    KW_OVERRIDING, // overriding - замещение операции
    KW_PACKAGE,    // package - пакет
    KW_PARALLEL,   // parallel - параллельное выполнение
    KW_PRAGMA,     // pragma - директива компилятора
    KW_PRIVATE,    // private - закрытая часть пакета
    KW_PROCEDURE,  // procedure - процедура
    KW_PROTECTED,  // protected - защищённый объект
    KW_RAISE,      // raise - возбуждение исключения
    KW_RANGE,      // range - диапазон значений
    KW_RECORD,     // record - запись (структура)
    KW_REM,        // rem - остаток от деления
    KW_RENAMES,    // renames - переименование
    KW_REQUEUE,    // requeue - перенаправление вызова
    KW_RETURN,     // return - возврат из подпрограммы
    KW_SELECT,     // select - избирательный ожидающий оператор
    KW_SEPARATE,   // separate - отдельная компиляция тела
    KW_SOME,       // some - частичное соответствие
    KW_SUBTYPE,    // subtype - подтип
    KW_SYNCHRONIZED, // synchronized - синхронизированный
    KW_TAG,        // tag - тег (идентификатор типа)
    KW_TASK,       // task - задача (легковесный процесс)
    KW_TERMINATE,  // terminate - завершение задачи
    KW_THEN,       // then - часть условного оператора
    KW_TYPE,       // type - объявление типа
    KW_UNTIL,      // until - условие выхода из цикла
    KW_USE,        // use - импорт пакета
    KW_WHEN,       // when - условие в case/accept
    KW_WHILE,      // while - цикл с предусловием
    KW_WITH,       // with - подключение пакета
    KW_XOR,        // xor - исключающее ИЛИ

    // ==================== ИДЕНТИФИКАТОРЫ И ЛИТЕРАЛЫ ====================
    IDENT,         // Идентификатор (имя переменной, константы, типа)
    INT,           // Целочисленный литерал (десятичный или на основе)
    REAL,          // Вещественный литерал (с десятичной точкой)
    CHAR,          // Символьный литерал (заключён в одинарные кавычки)
    STRING,        // Строковый литерал (заключён в двойные кавычки)

    // ==================== МУЛЬТИСИМВОЛЬНЫЕ ОПЕРАТОРЫ ====================
    ASSIGN,        // :=  (оператор присваивания)
    ARROW,         // =>  (стрелка, ассоциация)
    DOTDOT,        // ..  (диапазон значений)
    POW,           // **  (возведение в степень)
    BOX,           // <>  (пустой ящик, параметр по умолчанию)
    GE,            // >=  (больше или равно)
    LE,            // <=  (меньше или равно)
    NEQ,           // /=  (не равно)
    SHIFT_L,       // <<  (левый сдвиг, редко но возможно)
    SHIFT_R,       // >>  (правый сдвиг)

    // ==================== ОДНОСИМВОЛЬНЫЕ ОПЕРАТОРЫ и РАЗДЕЛИТЕЛИ ====================
    PLUS,          // +   (сложение/унарный плюс)
    MINUS,         // -   (вычитание/унарный минус)
    STAR,          // *   (умножение)
    SLASH,         // /   (деление)
    EQ,            // =   (сравнение на равенство)
    LT,            // <   (меньше)
    GT,            // >   (больше)
    AMP,           // &   (конкатенация строк)
    TICK,          // '   (штрих - атрибут, квалификатор)
    LPAREN,        // (   (левая круглая скобка)
    RPAREN,        // )   (правая круглая скобка)
    LBRACK,        // [   (левая квадратная скобка)
    RBRACK,        // ]   (правая квадратная скобка)
    LBRACE,        // {   (левая фигурная скобка)
    RBRACE,        // }   (правая фигурная скобка)
    SEMICOL,       // ;   (точка с запятой - завершитель оператора)
    COLON,         // :   (двоеточие - метка, константа)
    COMMA,         // ,   (запятая - разделитель параметров)
    DOT,           // .   (точка - компонент записи/пакета)
    BAR,           // |   (вертикальная черта)

    // ==================== СПЕЦИАЛЬНЫЕ ТОКЕНЫ ====================
    EOF,           // End Of File - конец входного файла
    ERROR          // Лексическая ошибка
}